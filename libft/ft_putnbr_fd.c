#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	if (n == 2147483647)
		ft_putstr_fd("2147483647", fd);
	if (n < 0 && n > -2147483648)
	{
		ft_putchar_fd('-', fd);
		n = n * (-1);
	}
	if (n < 10 && n > -2147483648)
	{
		ft_putchar_fd('0' + n, fd);
	}
	if (n >= 10 && n < 2147483647)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putchar_fd('0' + n % 10, fd);
	}
}
